void intakeIn()
{
	motor[intake] = 127;
}

void intakeOut()
{
	motor[intake] = -127;
}

void shoot()
{
	motor[puncher] = 127;
	wait1Msec(1000);
	motor[puncher] = 0;
}

void pUp()
{
	motor[adjuster] = 127;
	wait1Msec(500);
	motor[adjuster] = 0;
}

void pDown()
{
	motor[adjuster] = -127;
	wait1Msec(500);
	motor[adjuster] = 0;
}

void liftUp()
{
	motor[lift] = 127;
}

void liftDown()
{
	motor[lift] = -127;
}

void stopAll()
{
	motor[intake] = 0;
	motor[lift] = 0;
	motor[adjuster] = 0;
	motor[puncher] = 0;
}

