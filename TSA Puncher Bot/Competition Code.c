task usercontrol()
{
	while (true)
	{
		// Starts tasks from Deadzone Drive.c
		startTask (rightDrive);
		startTask (leftDrive);

		if (vexRT(Btn7R) == 1)
		{
			motor[lift] = 127;
		}
		else if (vexRT(Btn7D) == 1)
		{
			motor[lift] = -127;
		}
		else
		{
			motor[lift] = 0;
		}

		if (vexRT(Btn5U) == 1)
		{
			motor[intake] = 127;
		}
		else if (vexRT(Btn5D) == 1)
		{
			motor[intake] = -127;
		}
		else
		{
			motor[intake] = 0;
		}

		if (vexRT(Btn6U) == 1)
		{
			motor[puncher] = 127;
		}
		else
		{
			motor[puncher] = 0;
		}

		if (vexRT(Btn8U) == 1)
		{
			motor[adjuster] = 127;
		}
		else if (vexRT(Btn8D) == 1)
		{
			motor[adjuster] = -127;
		}
		else
		{
			motor[adjuster] = 0;
		}
	}
}
